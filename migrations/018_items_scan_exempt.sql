-- =====================================================
-- 018. items.scan_exempt (스캔 불필요 품목)
-- =====================================================
-- 로컬/배포: Cloud SQL(서울) — psql 또는 Cloud SQL Studio에서 실행
--
-- 배경:
--   "(주문서동봉)" 같은 동봉/안내 인쇄물은 작업자가 챙기되 바코드 스캔 대상이
--   아니다. 이런 품목을 scan_exempt=TRUE로 표시해:
--     - 검수 진행률(N/M)과 완료 판정에서 제외
--     - 검수 화면 카드에 "스캔 불필요" 배지 표시
--
-- 켜는 방법:
--   - 자동: 업로드/대량등록 시 이름에 "동봉" 키워드가 있으면 TRUE
--   - 수동: 품목 등록/수정 모달의 체크박스
--
-- 기존 품목은 전부 FALSE(기본값). 재실행 안전.
-- =====================================================

ALTER TABLE items
  ADD COLUMN IF NOT EXISTS scan_exempt BOOLEAN NOT NULL DEFAULT FALSE;
