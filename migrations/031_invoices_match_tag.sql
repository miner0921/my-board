-- =====================================================
-- 로컬/배포: Cloud SQL(서울) — psql 또는 Cloud SQL Studio에서 실행
--
-- 목적: 등록 시 이미 계산되는 매칭 분류를 "기록만" 한다(매칭 로직 불변).
--   값: 'matched'      = 발주서와 매칭된 송장
--       'invoice_only' = 송장에만 있음(발주서 없음, 그래도 등록됨)
--   (발주서에만 있는 건은 송장이 없어 invoices에 안 들어감 → 032의 batch 집계로)
--
-- nullable → 기존 송장은 NULL. 재실행 안전(IF NOT EXISTS).
-- =====================================================

ALTER TABLE invoices
  ADD COLUMN IF NOT EXISTS match_tag VARCHAR(20);
