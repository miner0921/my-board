-- =====================================================
-- 016. items.barcode UNIQUE 제약 해제 (바코드 중복 허용)
-- =====================================================
-- 로컬/배포: Cloud SQL(서울) — psql 또는 Cloud SQL Studio에서 실행
--
-- 배경:
--   같은 바코드를 쓰는 서로 다른 품목이 존재함
--   (예: "1팩 피치앤블랙티" 와 "샘플 피치앤블랙티"가 동일 바코드).
--   기존 UNIQUE 제약 때문에 둘째 품목 등록이 막혀서 제약을 해제한다.
--
-- 영향:
--   - 검수 스캔은 "전역에서 바코드로 품목 1개" → "현재 송장 품목 중 매칭"으로
--     변경됨(app/api/warehouse/scan/route.ts). 따라서 전역 중복이 있어도
--     검수는 현재 송장의 품목으로 정확히 잡힌다.
--   - 조회 성능용 비유니크 인덱스 idx_items_barcode(001)는 그대로 유지.
--
-- UNIQUE 제약명은 컬럼 정의(barcode VARCHAR(100) UNIQUE)로 자동 생성된
--   items_barcode_key 이다. 재실행 안전(IF EXISTS).
-- =====================================================

ALTER TABLE items DROP CONSTRAINT IF EXISTS items_barcode_key;
