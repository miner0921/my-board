-- =====================================================
-- 021. scan_logs.quantity — 이벤트별 변화량 기록
-- =====================================================
-- 로컬/배포: Cloud SQL(서울) — Cloud SQL Studio 또는 psql에서 실행
--
-- 스캔 기록 타임라인에 "몇 개" 변했는지 보여주기 위한 컬럼.
--   - quantity = 그 이벤트의 변화량(절대값)
--       · 정상 스캔 / 초과 스캔 / 현장 추가: 1 (그 스캔의 +1)
--       · 수동 추가(manual_add): 늘어난 수량 (후 - 전)
--       · 수동 취소(manual_remove): 줄어든 수량 (전 - 후)
--       · 품목 취소(item_excluded): 취소된 수량(scanned_count)
--       · 복구(item_restored): 복구된 수량
--   - 진짜 오류(미등록/송장없음/송장에없는품목)는 수량 변화 없음 → NULL
--
-- 기존 행은 전부 NULL(=수량정보 없음, 소급 표시 안 됨). 재실행 안전.
-- =====================================================

ALTER TABLE scan_logs ADD COLUMN IF NOT EXISTS quantity INTEGER;
